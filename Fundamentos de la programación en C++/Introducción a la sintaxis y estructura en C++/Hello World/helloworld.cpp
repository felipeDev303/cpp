// Incluimos la librería estándar de flujo de entrada-salida para ingresar y mostrar datos 
#include <iostream>

//Main function, punto de entrada a la app
int main(){
    std::cout << "¡Hola Mundo!" << std::endl;
    return 0; 

}

/*
std::cout: objeto estándar para enviar datos a la consola

<<: operador de inserción del flujo, dirige la salida al flujo

"¡Hola Mundo!": string que deseo mostrar

<< std::endl;: nueva línea espues del output para formatear
*/